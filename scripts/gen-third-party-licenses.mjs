#!/usr/bin/env node
/**
 * Generate THIRD_PARTY_LICENSES.md from the actual LICENSE files of the
 * RUNTIME dependencies that get bundled into the MCPB artifact (esbuild
 * concatenates them into a single server/index.js). Reproduces each license
 * in full so the bundle complies with MIT / Apache-2.0 attribution terms.
 *
 *   node scripts/gen-third-party-licenses.mjs
 */
import { readFileSync, writeFileSync, existsSync, statSync } from "node:fs";

// Read a package's manifest directly (require() can be blocked by an `exports`
// map that hides ./package.json).
function readPkg(dep) {
  return JSON.parse(readFileSync(`node_modules/${dep}/package.json`, "utf8"));
}

// Runtime deps that end up in the bundle (devDeps like sharp/typescript are
// build-only and are NOT shipped, so they're excluded).
const DEPS = ["@modelcontextprotocol/sdk", "replicate", "zod"];

function licenseText(dep) {
  for (const f of ["LICENSE", "LICENSE.md", "LICENSE.txt", "license"]) {
    const p = `node_modules/${dep}/${f}`;
    if (existsSync(p)) return readFileSync(p, "utf8").trim();
  }
  return `(license text not found in the installed package; see https://www.npmjs.com/package/${dep})`;
}

let out =
  "# Third-Party Licenses\n\n" +
  "`replicate-mcp-server` is MIT-licensed (see [LICENSE](LICENSE)). It depends on the " +
  "open-source packages below.\n\n" +
  "When distributed as a **bundled MCPB** (`mcpb/replicate-mcp-server.mcpb` — a single " +
  "esbuild-concatenated `server/index.js`), the source of these runtime dependencies is " +
  "included in that artifact, so their licenses are reproduced here in full to satisfy the " +
  "MIT / Apache-2.0 attribution requirements. The npm package ships unbundled (`dist/` imports " +
  "these from `node_modules` at install time), but the same attributions apply.\n\n" +
  "_Generated by `scripts/gen-third-party-licenses.mjs`._\n\n";

for (const dep of DEPS) {
  const pkg = readPkg(dep);
  const license = typeof pkg.license === "string" ? pkg.license : JSON.stringify(pkg.license);
  const home = pkg.homepage || `https://www.npmjs.com/package/${dep}`;
  out += `---\n\n## ${dep} \`${pkg.version}\` — ${license}\n\n`;
  out += `Homepage: ${home}\n\n`;
  out += "```\n" + licenseText(dep) + "\n```\n\n";
}

writeFileSync("THIRD_PARTY_LICENSES.md", out);
console.log(`wrote THIRD_PARTY_LICENSES.md (${statSync("THIRD_PARTY_LICENSES.md").size} bytes, ${DEPS.length} deps)`);
